package NewPackage;

public class Casting_Class {
	
	// implicit casting lower data types to higher data types
			// means int to double/float/long
			
			int a=20; // creat variable 
			double b=a;
			
			public void m1() // --> creat method
			{
				System.out.println(b);
			}
			
			

	public static void main(String[] args) 
	{  
		Casting_Class rushika = new Casting_Class();
		rushika.m1();		
		
	}

}
