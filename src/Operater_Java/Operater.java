package Operater_Java;

public class Operater {

	public static void main(String[] args) {
		//Arithematic Operator:
		int a = 10;
		int b =30;
		
		System.out.println(a+b); // RESULT A + B
		
		System.out.println(a-b); // Result A - B
		
		System.out.println(a/b); // Result A / B
	
		
		System.out.println(a*b); // Result A * B
		
		System.out.println(a%b);  // Result A % B
				
		
		
		
		
		

	}

}
